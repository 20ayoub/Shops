package com.example.model;

public class Point {
	Double x;
	Double y;
	
	//Constructor
	public Point(Double x, Double y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	
	
	
	//getters and setters
	public Double getX() {
		return x;
	}
	public void setX(Double x) {
		this.x = x;
	}
	public Double getY() {
		return y;
	}
	public void setY(Double y) {
		this.y = y;
	}
	
	
}
